# List Data type
list1=[2023, "Python", 3.11, 5+6j, 1.23E-4]
print(type(list1))  # Output: <class 'list'>

# Tuple Data type
tuple1=(2023, "Python", 3.11, 5+6j, 1.23E-4)
print(type(tuple1))  # Output: <class 'tuple'>

# Set Data type
set1={2023, "Python", 3.11, 5+6j, 1.23E-4}
print(type(set1))  # Output: <class 'set'>

# Dictionary Data type
dict1={"year": 2023, "language": "Python", "version": 3.11, "complex": 5+6j, "scientific": 1.23E-4}
print(type(dict1))  # Output: <class 'dict'>